import axios from "axios";

const baseUrl='http://localhost:8080/tareas';

export const findAll = async () => {
    try{
        const response = await axios.get(baseUrl);
        return response;
    } catch (error){
        console.log(error);
    }
    return null;
}
export const listTareaCompletada = async () => {
    try{
        const response = await axios.get(baseUrl + '/completada');
        return response;
    } catch (error){
        console.log(error);
    }
    return null;
}
export const listTareaNoCompletada = async () => {
    try{
        const response = await axios.get(baseUrl + '/sinCompletar');
        return response;
    } catch (error){
        console.log(error);
    }
    return null;
}

export const create = async ({titulo, descripcion, completada}) => {
    try{
        const response = await axios.post(baseUrl, {titulo:titulo, descripcion:descripcion, completada:completada});
        return response;
    } catch (error){
        console.log(error);
    }
    return undefined;
}
export const update = async ({id, titulo, descripcion, completada}) => {
    try{
        const response = await axios.put(baseUrl + '/'+id, {titulo:titulo, descripcion:descripcion, completada:completada});
        return response;
    } catch (error){
        console.log(error);
    }
    return undefined;
}
export const remove = async (id) => {
    try{
        await axios.delete(baseUrl + '/'+id);
    } catch (error){
        console.log(error);
    }
}