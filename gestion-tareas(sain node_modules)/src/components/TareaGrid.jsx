/* eslint-disable react/prop-types */
import { TareaDetail } from "./TareaDetail"
import { TareaDetailTable } from "./TareaDetailTable"

export const TareaGrid = ({ handlerSelected, handlerRemove, tareas, tareasCompletadas = [] }) => {
    let numCompletas = 0;
    let numNoCompletas = 0;
    return (
        
        <div>
            {tareas.map(tarea => {
                return (<TareaDetail handlerRemove={handlerRemove} handlerSelected={handlerSelected} tarea={tarea} key={tarea.id} />
                )
            })}
            <table>
                <thead>
                    <tr>
                        <th>Titulo</th>
                        <th>Descripcion</th>
                    </tr>
                </thead>
                <tbody>
                    {tareasCompletadas.map(tarea => {
                        numCompletas++;
                        return <TareaDetailTable tarea={tarea} key={tarea.id} />
                    })}
                </tbody>
                <p>Numero de tareas completadas: {numCompletas}</p>
                <p>Numero de tareas no completadas: {numNoCompletas}</p>
            </table>
        </div>

    )
}