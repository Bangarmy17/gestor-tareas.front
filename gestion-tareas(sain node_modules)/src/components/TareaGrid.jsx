import { TareaDetail } from "./TareaDetail"

// eslint-disable-next-line react/prop-types
export const TareaGrid = ({ handlerSelected,handlerRemove, tareas = [] }) => {
    return(
        <div>
             {tareas.map(tarea => {
            return <TareaDetail handlerRemove={handlerRemove} handlerSelected={handlerSelected} tarea={tarea} key={tarea.id}/>
        })}
        </div>
    )
}