// eslint-disable-next-line react/prop-types
export const TareaDetail = ({ handlerSelected, handlerRemove, tarea = {},}) => {
    return (
        <div>
            <li>{tarea.nombre} {tarea.descripcion} <input type="checkbox" placeholder="Completada"
                name="completada"
                checked={tarea.completada}
                disabled /> <button onClick={() => handlerRemove(tarea.id)}>Eliminar</button>
                <button onClick={() => handlerSelected(tarea)}>Editar</button></li>
        </div>

    )
}