import { useState } from 'react';

const initialDataForm = {
    nombre: '',
    descripcion: '',
    completa: false
};

export const TareaForm = ({handlerAdd}) => {
    const [form, setForm] = useState(initialDataForm);
    const { nombre, descripcion, completa } = form;
    return (
        <form onSubmit={
            (event) => {
                event.preventDefault();
                if (!nombre || !descripcion) {
                    alert('Datos incompletos');
                    return;
                }
                handlerAdd(form);
                setForm(initialDataForm);
            }
        }>
            <div>
                <input
                    type="text"
                    name="nombre"
                    value={nombre}
                    onChange={(event) => setForm({ ...form, nombre: event.target.value })}
                    placeholder="Nombre"
                />
            </div>
            <div>
                <input
                    type="text"
                    name="descripcion"
                    value={descripcion}
                    onChange={(event) => setForm({ ...form, descripcion: event.target.value })}
                    placeholder="DescripciÃ³n"
                />
            </div>
            <div>
                <input
                    type="checkbox"
                    name="completa"
                    checked={completa}
                    onChange={(event) => setForm({ ...form, completa: event.target.checked })}
                />
            </div>
            <div>
                <button type="submit">Agregar</button>
            </div>
        </form>
    )
}